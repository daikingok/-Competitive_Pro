	private static double dist( double x1, double y1, double x2, double y2 ) {
		double l;
		double dx,dy;
		dx = x2 - x1;
		dy = y2 - y1;
		l = Math.sqrt( dx * dx + dy * dy );
		return l;
	}
	public static int calcMax(int[] array) {

        int intMax = array[0];

        for (int i = 1; i < array.length; i++ ) {
            if(intMax < array[i]) {
                intMax = array[i];
            }
        }
        return intMax;
    }

    public static int calcMin(int[] array) {

        int intMin = array[0];

        for (int i = 1; i < array.length; i++ ) {
            if(intMin > array[i]) {
                intMin = array[i];
            }
        }
        return intMin;
    }

		
	

